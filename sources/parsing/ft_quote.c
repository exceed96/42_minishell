#include "../../includes/minishell.h"

void	ft_quote(char *str, int i, int *c)
{
	if (str[i] && str[i] == '\"' && !c[0] && !c[1])
		c[0] = 1;
	else if (str[i] && str[i] == '\'' && !c[0] && !c[1])
		c[1] = 1;
	else if (str[i] && str[i] == '\"' && c[0] && !c[1])
		c[0] = 0;
	else if (str[i] && str[i] == '\'' && !c[0] && c[1])
		c[1] = 0;
}
